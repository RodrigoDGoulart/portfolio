type BadgeName = keyof typeof BADGES;

export function getBadgeData(name: string): BadgeType {
  const defaultBadge: BadgeType = {
    color: {
      primary: "red",
      secondary: "white",
    },
    icon: githubIcon,
    label: name,
  };
  return BADGES[name as BadgeName] ?? defaultBadge;
}